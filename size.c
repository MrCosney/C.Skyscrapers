int		size(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc != 2)
		return (-1);
	while (argv[1][i] != '\0')
		i++;
	if (((i + 1) % 8) != 0)
		return (-1);
	i = ((i + 1) / 8);
	return (i);
}
